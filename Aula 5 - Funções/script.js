//Funções
/*
São blocos de comandos e instruões para a execução de determinadas tarefas:

Ex.: function nomeDaFuncao(){
  ${instrucao};
}

nomeDaFuncao();


function funcao() {
  console.log('Tudo certo jovens!');
}

funcao();

*/
//Como declarar?
/*
 Geralmente se utiliza a palavra reservada "function" seguida de parênteses "()" e chaves "{}":

 Ex.: function funcao(){
   console.log("mensagem");
 }

 funcao();

 () - indica que é um objeto do tipo function;
 {} - indica que é um bloco de instrução;
 
 */
//Funções com parâmetros
/*
 As funções podem receber em sua declaraão, parâmetros, que servem como variáveis, onde sua atribuição pode ser feita durante a chamada da função;

 Ex.: function nomeDaFuncao(parametro){
   ${instrucao};
 }

 nomeDaFuncao(valorDoParametro);

 function mensagem(primeiro, segundo){
   console.log(primeiro, segundo);
 }

 mensagem('Tudo certo', ', jovem!');

 */
